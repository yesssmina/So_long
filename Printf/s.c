#include "ft_printf.h"

void	ft_s(char *s, int	*print)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		*print += 6;
		return ;
	}
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		(*print)++;
		i++;
	}
}
