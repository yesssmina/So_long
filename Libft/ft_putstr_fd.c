#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		i;
	char	lettre;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		lettre = s[i];
		write(fd, &lettre, 1);
		i++;
	}
}
